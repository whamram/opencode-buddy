export function renderCorrupted(frame = 0): string[] {
  const activeFrame = CORRUPTED[frame % CORRUPTED.length]!;
  return [...activeFrame];
}

export function renderSprite(bones: CompanionBones, frame = 0): string[] {
  const frames = BODIES[bones.species];
  const body = frames[frame % frames.length]!.map((line) =>
    line.replaceAll('{E}', bones.eye),
  );
  const lines = [...body];
  if (bones.hat !== 'none' && !lines[0]!.trim()) {
    lines[0] = HAT_LINES[bones.hat];
  }
  if (!lines[0]!.trim() && frames.every((f) => !f[0]!.trim())) lines.shift();
  return lines;
}

export function spriteFrameCount(species: Species): number {
  return BODIES[species].length;
}

export function renderFace(bones: CompanionBones): string {
  const eye: Eye = bones.eye;
  switch (bones.species) {
    case 'duck':
    case 'goose':
      return `(${eye}>`;
    case 'blob':
      return `(${eye}${eye})`;
    case 'cat':
      return `=${eye}ŌČ${eye}=`;
    case 'dragon':
      return `<${eye}~${eye}>`;
    case 'octopus':
      return `~(${eye}${eye})~`;
    case 'owl':
      return `(${eye})(${eye})`;
    case 'penguin':
      return `(${eye}>)`;
    case 'turtle':
      return `[${eye}_${eye}]`;
    case 'snail':
      return `${eye}(@)`;
    case 'ghost':
      return `/${eye}${eye}\\`;
    case 'axolotl':
      return `}${eye}.${eye}{`;
    case 'capybara':
      return `(${eye}oo${eye})`;
    case 'cactus':
      return `|${eye}  ${eye}|`;
    case 'robot':
      return `[${eye}${eye}]`;
    case 'rabbit':
      return `(${eye}..${eye})`;
    case 'mushroom':
      return `|${eye}  ${eye}|`;
    case 'chonk':
      return `(${eye}.${eye})`;
  }
}